    private void btnAvançarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvançarActionPerformed
       try{
        if (!selecionado(lblTitle).equals("null")) {
            String result = selecionado(lblTitle);
            objArquivo = new Arquivo("Personagem.txt");
            objArquivo.abrirEscrita();
            String linha = "";
            linha = result;
            objArquivo.escreverLinha(linha);
            objArquivo.fecharArquivo();
            
            TelaNivel1 tela5 = new TelaNivel1();
            tela5.setVisible(true);
            dispose();
        
        }
       }catch(Exception ex){
          Globais.exibirMensagem("Você precisa selecionar um personagem para avançar!");
       }
    }//GEN-LAST:event_btnAvançarActionPerformed

