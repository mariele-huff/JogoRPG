    private static String selecionado(JLabel label) {
        if (label.getText().equals("Marie Curie")) {
            return "Marie Curie";
        } else if (label.getText().equals("Nikola Tesla")) {
            return "Nikola Tesla";
        } else if (label.getText().equals("Charles Darwin")) {
            return "Charles Darwin";
        } else if (label.getText().equals("Albert Einstein")) {
            return "Albert Einstein";
        } else if (label.getText().equals("Ada Lovelace")) {
            return "Ada Lovelace";
        } else if (label.getText().equals("Sigmund Freud")) {
            return "Sigmund Freud";
        } else {
            return null;
        }
    }

